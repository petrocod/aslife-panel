-- ب) از خود اپ (/login یا ثبت‌نام): با همان ایمیل ثبت‌نام کنید؛ رمزی که انتخاب
--    کردید همان ورود است. بعد این UPDATE را اجرا کنید تا company به دمو وصل شود.
